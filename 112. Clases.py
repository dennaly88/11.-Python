class Rectangulo:
    def __init__(self, base, altura):
        self.base = base
        self.altura = altura

    def area(self):
        return self.base * self.altura

    def perimetro(self):
        return 2 * (self.base + self.altura)

b = float(input("Base: "))
h = float(input("Altura: "))
r = Rectangulo(b, h)
print(f"Área: {r.area()}  |  Perímetro: {r.perimetro()}")


